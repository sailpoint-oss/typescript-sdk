const fs = require("fs");
const path = require("path");

const getAllFiles = function (dirPath, arrayOfFiles) {
  const files = fs.readdirSync(dirPath);
  arrayOfFiles = arrayOfFiles || [];
  files.forEach(function (file) {
    if (fs.statSync(dirPath + "/" + file).isDirectory()) {
      arrayOfFiles = getAllFiles(dirPath + "/" + file, arrayOfFiles);
    } else {
      arrayOfFiles.push(path.join(dirPath, file));
    }
  });
  return arrayOfFiles;
};

// When an OpenAPI schema name already contains a version suffix (e.g. AccessRequestConfigV2),
// the generator lowercases it to "accessrequestconfigv2" and then appends the
// --model-name-suffix "V1", producing "Accessrequestconfigv2V1".
// This function corrects those names back to "AccessrequestconfigV2":
//   Pattern: word ending in  v<digits>V1  →  replace with  V<digits>
const fixVersionedModelNames = function (content) {
  return content.replace(/([A-Z]\w*)v(\d+)V1\b/g, "$1V$2");
};

const fixFiles = function (myArray) {
  for (const file of myArray) {
    if (!file.endsWith(".ts")) continue;
    const original = fs.readFileSync(file, "utf8");
    const fixed = fixVersionedModelNames(original);
    if (fixed !== original) {
      fs.writeFileSync(file, fixed, "utf8");
    }
  }
};

// Merge all per-operation developerSite_code_examples.yaml files generated by
// the APIDocs templateType into a single go_code_examples_overlay.yaml, then
// remove the originals so only the merged file remains.
const mergeCodeExampleFiles = function (docsExamplesDir) {
  if (!fs.existsSync(docsExamplesDir)) return;

  const files = fs.readdirSync(docsExamplesDir).filter(f => f.endsWith(".yaml") && f !== "typescript_code_examples_overlay.yaml");
  if (files.length === 0) return;

  let merged = "";
  for (const file of files) {
    const filePath = path.join(docsExamplesDir, file);
    merged += fs.readFileSync(filePath, "utf8");
    fs.unlinkSync(filePath);
  }

  fs.writeFileSync(path.join(docsExamplesDir, "typescript_code_examples_overlay.yaml"), merged, "utf8");
  console.log(`Merged ${files.length} code example file(s) into typescript_code_examples_overlay.yaml`);
};

// Move generated doc files from the root output dir into docs/Methods and docs/Models,
// then assemble the docs/Examples directory for code samples.
const organiseDocs = function (outputDir) {
  const docsMethodsDir  = path.join(outputDir, "docs", "Methods");
  const docsModelsDir   = path.join(outputDir, "docs", "Models");
  const docsExamplesDir = path.join(outputDir, "docs", "Examples");

  fs.mkdirSync(docsMethodsDir,  { recursive: true });
  fs.mkdirSync(docsModelsDir,   { recursive: true });
  fs.mkdirSync(docsExamplesDir, { recursive: true });

  // APIDocs templates generate files at the root as <ClassName><destinationFilename>.
  // api_doc.mustache      → <ClassName>.md       (e.g. AccountsV1Api.md)  → Methods
  // model_doc.mustache    → <ClassName>.md        (e.g. AccountV1.md)      → Models
  // code_examples.mustache→ <ClassName>*.yaml                              → Examples
  // Index.md              → docs_models_index output                       → Models
  // README.md             → standard generator README                      → Models
  //
  // API class names always end with the api-name-suffix "V1Api" (or similar),
  // so files ending in "Api.md" are API method docs; everything else is a model doc.
  const rootFiles = fs.readdirSync(outputDir);
  for (const file of rootFiles) {
    const src = path.join(outputDir, file);
    if (fs.statSync(src).isDirectory()) continue;

    if (file.endsWith(".yaml")) {
      // Code example files (e.g. AccountsV1ApideveloperSite_code_examples.yaml)
      fs.renameSync(src, path.join(docsExamplesDir, file));
    } else if (file.endsWith(".md")) {
      if (file.endsWith("Api.md")) {
        // API method doc — route to Methods
        fs.renameSync(src, path.join(docsMethodsDir, file));
      } else {
        // Index.md (models index) and all per-model docs — route to Models
        fs.renameSync(src, path.join(docsModelsDir, file));
      }
    }
  }

  // Move any remaining .md files from the docs/ subdirectory (e.g. Methods/Index.md
  // is already placed directly by the SupportingFiles config, so nothing to do there).
  const generatedDocsDir = path.join(outputDir, "docs");
  if (fs.existsSync(generatedDocsDir)) {
    for (const file of fs.readdirSync(generatedDocsDir)) {
      const src = path.join(generatedDocsDir, file);
      if (fs.statSync(src).isDirectory()) continue;
      if (file.endsWith(".yaml")) {
        fs.renameSync(src, path.join(docsExamplesDir, file));
      } else if (file.endsWith(".md") && file !== "Index.md") {
        fs.renameSync(src, path.join(docsMethodsDir, file));
      }
    }
  }

  mergeCodeExampleFiles(docsExamplesDir);
};


const outputDir = process.argv[2];

let myArray = [];
getAllFiles(outputDir, myArray);

fixFiles(myArray);
organiseDocs(outputDir);